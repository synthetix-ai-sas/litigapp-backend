        await SeedCitiesAsync(db, ct);
        await SeedEntitiesAsync(db, ct);
        await SeedSpecialtiesAsync(db, ct);
        // Courts last: FKs to cities (required), entities and specialties (nullable).
        await SeedCourtsAsync(db, ct);
    }

